numero = int(input("Introduce un numero entero positivo: "))

if numero == 0:
    print("Cero")
elif numero == 1:
    print("Uno")
elif numero == 2:
    print("Dos")
elif numero == 3:
    print("Tres")
elif numero == 4:
    print("Cuatro")
elif numero == 5:
    print("Cinco")
elif numero == 6:
    print("Seis")
elif numero == 7:
    print("Siete")
elif numero == 8:
    print("Ocho")
elif numero == 9:
    print("Nueve")
else:
    print("Otros")